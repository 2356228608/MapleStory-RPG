function monsterValue(eim, mobId) {
	// em.broadcastServerMsg("[monsterValue]=" + mobId);
	var killReq = 100;
	var kill = parseInt(em.getProperty("kill")) + 1;
	em.setProperty("kill", kill);
	eim.getPlayers().forEach(function (player) {
		var mapId = player.getMapId();
		if (kill >= killReq) {
			var map = em.getMapFactoryMap(mapIds[Math.round((mapId - 211080100) / 100)]);
			if (mapId == 211080300 || mapId == 211080600) {
				player.dropMessage(-1, "感受到敌人的气息，石头人王出现了。");
				var mob = em.getMonster(8211004);
			} else {
				player.dropMessage(-1, "感受到生命的旺盛血气，黄金蜜蜂出现了。");
				var mob = em.getMonster(8211003);
			}
			eim.registerMonster(mob);
			map.spawnMonsterOnGroundBelow(mob, new java.awt.Point(randomNum(-100, 3200), -200));
			em.setProperty("kill", 0);
		} else {
			player.dropMessage(-1, "消灭怪物 " + kill + " / " + killReq + " 只后，似乎会刺激到什么存在");
		}
		var score = mobId == 8211004 ? 50 : mobId == 8211003 ? 25 : 3;
		var data = getData(player, 31035, ["score", "total"]);
		data[0][1] = parseInt(data[0][1]) + score;
		saveData(player, 31035, data);
	});
	return 1;
}

